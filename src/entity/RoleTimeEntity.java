package entity;

public class RoleTimeEntity {
	private String vname;
	private String dortime;
	private String daxtime;
	private String phutime;
	private String xfutime;
	private String jxtime;
	
	public String getVname() {
		return vname;
	}
	public void setVname(String vname) {
		this.vname = vname;
	}
	public String getDortime() {
		return dortime;
	}
	public void setDortime(String dortime) {
		this.dortime = dortime;
	}
	public String getDaxtime() {
		return daxtime;
	}
	public void setDaxtime(String daxtime) {
		this.daxtime = daxtime;
	}
	public String getPhutime() {
		return phutime;
	}
	public void setPhutime(String phutime) {
		this.phutime = phutime;
	}
	public String getXfutime() {
		return xfutime;
	}
	public void setXfutime(String xfutime) {
		this.xfutime = xfutime;
	}
	public String getJxtime() {
		return jxtime;
	}
	public void setJxtime(String jxtime) {
		this.jxtime = jxtime;
	}
}
